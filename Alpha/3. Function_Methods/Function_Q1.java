import java.util.*;

public class Function_Q1 {
    public static void main(String[] args) {
        calculateSum();
    }
    public static void calculateSum () {
        Scanner sc = new Scanner(System.in);
        int a=sc.nextInt();
        int b = sc.nextInt();
        int sum = a+b;
        System.out.println("Sum is : "+sum);
    }
}
