//WAJP to check number is divisible by 6 without dividing by six.

class Q6_If_Else_Condition{
	public static void main(String[] args) {
		int a=19;
		if(a%2==0&&a%3==0)
			System.out.println(a+" is divisible by 6.");
		else
			System.out.println(a+" is Not divisible by 6.");
	}
} 
// output- 19 is Not divisible by 6.