//4. WAJP to convert the double value (56.09) into int type and print it.

class Convert2
	{
		public static void main(String[] args)
		{
			double d=56.09;
			int i=(int)d;

			System.out.println("Double_value - "+d);
			System.out.println("Int_value - "+i);
		}
	}
/* output -
		Double_value - 56.09
		Int_value - 56
*/